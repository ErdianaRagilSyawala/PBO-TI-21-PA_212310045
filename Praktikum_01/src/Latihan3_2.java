
public class Latihan3_2 {

	public static void main(String[] args) {
		int r = 7, t = 10;
	    double l, v;
	    
	    l = 2 * 3.14 * r * (r + t);
	    v = 3.14 * r * r * t;
	    
	    System.out.println("Luas permukaan tabung adalah = " + l);
	    System.out.println("Volume tabung adalah = " + v);

	}

}
